const compose =
  (...funcs) =>
  (component) => {
    return funcs.reduceRight((prev, func) => func(prev), component);
  };

const generateId = () => {
  var S4 = function () {
    return (((1 + Math.random()) * 0x10000) | 0).toString(16).substring(1);
  };
  return (
    S4() +
    S4() +
    "-" +
    S4() +
    "-" +
    S4() +
    "-" +
    S4() +
    "-" +
    S4() +
    S4() +
    S4()
  );
};

export { compose, generateId };
